public class Arco<E extends Comparable<E>>{
//---------------------------------------------------------------------------------------------		
//atributos------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------	
	public int costo;		
	public Nodo<E> destino;
//---------------------------------------------------------------------------------------------			
//constructor----------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------	
	public Arco(){
		
		destino = null;
		costo = 0;
	}
//---------------------------------------------------------------------------------------------	
//Metodos--------------------------------------------------------------------------------------	
//---------------------------------------------------------------------------------------------	
	public Arco (Nodo<E> d, int c){
		
		destino = d;
		costo = c;		
	}
//---------------------------------------------------------------------------------------------	
	public int compareTo(E x){
			
			return destino.valor.compareTo(x);
		}
//---------------------------------------------------------------------------------------------	
	public boolean equals(Object objeto){

		if (this == objeto) return true;
		if (objeto == null) return false;
		if (objeto.getClass() != this.getClass() ) return false;

		Arco<E> arco = (Arco<E>) objeto;

		if (!(this.destino.equals(arco.destino))) return false;
		return true;
	}
}







